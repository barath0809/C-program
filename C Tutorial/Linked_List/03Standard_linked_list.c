#include "stdio.h"
#include "stdlib.h"

struct Node
{
    int data;
    struct Node *next;
};
/*#####################################################################################################################*/
/*         ||||||||||||                    PRINT_FUNCTION                         ||||||||||||                         */
/*#####################################################################################################################*/
struct Node print_fuction(struct Node *head)
 {
    while(head)
     {
       printf("%d\n",head->data);
       head =  head->next;  
     }
 }
/*#####################################################################################################################*/
/*         ||||||||||||                    SUB_FUNCTION                         ||||||||||||                         */
/*#####################################################################################################################*/
void insert(struct Node** head_ref,int data_final)
{
    struct Node* temp ;
    temp = (struct Node*)malloc(sizeof(struct Node));
    temp->data = data_final;
    temp->next = *head_ref; 
    *head_ref = temp ;
}

/*#####################################################################################################################*/
/*         ||||||||||||                NODE_CREATION_FUNCTION                         ||||||||||||                     */
/*#####################################################################################################################*/
 struct Node* create_list(int putti[],int max)
 {
    int i;
    struct Node* head = NULL;
    for(i = max-1;i>=0;i--)
    {
        insert(&head,putti[i]);
    }
    return head;
 }
/*#####################################################################################################################*/
/*         ||||||||||||                    MAIN_FUNCTION                         ||||||||||||                          */
/*#####################################################################################################################*/
 int main()
 {
    int members[]={23,64,86,56,77,35};
    int size = sizeof(members)/sizeof(members[0]);
     struct Node* head = create_list(members,size);
     print_fuction(head);
    return 0;
 }
